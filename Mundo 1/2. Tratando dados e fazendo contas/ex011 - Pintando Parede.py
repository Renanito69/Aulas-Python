n = float(input('qual e a base da parede? '))
n2 = float(input('qual e a altura da parede? '))
s = n * n2
s2 = s / 2

print('A area de uma parede de base {} e altura {} e {}M².'.format(n, n2, s))
print('Pra {}M² sao necessarios {}L de tintas'.format(s, s2))

