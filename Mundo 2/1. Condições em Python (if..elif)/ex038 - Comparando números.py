w = int(input('Qual o 1º Numero: '))
s = int(input('Qual o 2º Numero: '))

if w > s:
    print('O 1º Numero {} e Maior do que o 2º Numero {}'.format(w, s))
elif w < s:
    print('O 2º Numero {} e Maior do que o 1º Numero {}'.format(s, w))
else:
    print('Os Numeros 1º e 2º sao Iguais')

